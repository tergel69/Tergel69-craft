export function getItem(id: ItemType | string): ItemData | undefined {
  return ITEMS[id];
}

// Get all items for creative mode inventory
export function getAllItems(): ItemData[] {
  return Object.values(ITEMS);
}

// Get items by category
export function getItemsByCategory(category: 'tools' | 'armor' | 'materials' | 'food' | 'misc'): ItemData[] {
  return Object.values(ITEMS).filter(item => {
    switch (category) {
      case 'tools':
        return item.toolType !== undefined;
      case 'armor':
        return item.armorSlot !== undefined;
      case 'food':
        return item.foodPoints !== undefined;
      case 'materials':
        return !item.toolType && !item.armorSlot && !item.foodPoints && item.stackSize > 1;
      case 'misc':
        return !item.toolType && !item.armorSlot && !item.foodPoints && item.stackSize === 1;
    }
  });
}
