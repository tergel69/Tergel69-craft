export interface BlockData {
  id: BlockType;
  name: string;
  hardness: number; // Time to break with hand in seconds (0 = instant, -1 = unbreakable)
  transparent: boolean;
  solid: boolean;
  liquid: boolean;
  lightLevel: number; // 0-15
  textureTop: string;
  textureSide: string;
  textureBottom: string;
  tool?: 'pickaxe' | 'shovel' | 'axe' | 'shears' | 'sword' | 'hoe';
  drops?: { item: BlockType | string; count: number; chance?: number }[];
  color: string; // Hex color for texture generation
  colorTop?: string;
  colorBottom?: string;
  // New properties for proper rendering and collision
  isFullCube?: boolean; // Whether block occupies full 1x1x1 space (default: true for solid blocks)
  renderType?: 'cube' | 'cross' | 'plant' | 'torch' | 'liquid' | 'custom'; // How to render the block (default: 'cube')
  hitbox?: { x: number; y: number; z: number; width: number; height: number; depth: number }; // Custom collision box
}

